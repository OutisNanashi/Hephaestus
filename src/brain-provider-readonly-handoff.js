/** Run one Step 6M provider-brain → controlled prompt → read-only Codex handoff. */
export function runActivationProviderBrainReadonlyHandoff(request) {
  assertRequestShape(request);
  const adapterId = request.adapterId ?? "codex";
  if (adapterId !== "codex") fail(`Step 6M is only available for the codex adapter; received ${adapterId}.`, "STEP_6M_ADAPTER_NOT_ALLOWED");
  requireAdapter(adapterId);
  if (request.explicitProviderHandoffPermit !== true) {
    fail("Step 6M requires explicitProviderHandoffPermit=true.", "STEP_6M_PERMIT_REQUIRED");
  }
  if (typeof request.allowedRoot !== "string" || request.allowedRoot.length === 0) fail("Step 6M requires allowedRoot.", "INVALID_STEP_6M_REQUEST");
  if (typeof request.projectPath !== "string" || request.projectPath.length === 0) fail("Step 6M requires projectPath.", "INVALID_STEP_6M_REQUEST");
  if (typeof request.projectId !== "string" || !PROJECT_ID_PATTERN.test(request.projectId)) fail("Step 6M requires a safe projectId.", "INVALID_STEP_6M_REQUEST");
  if (request.reportName !== undefined && (typeof request.reportName !== "string" || !REPORT_NAME_PATTERN.test(request.reportName) || !request.reportName.endsWith(".json"))) {
    fail("Step 6M reportName must be a safe filename ending in .json.", "INVALID_STEP_6M_REQUEST");
  }

  const projectId = request.projectId;
  const allowedRoot = path.resolve(request.allowedRoot);
  const now = typeof request.now === "function" ? request.now : defaultNow;
  const spawn = typeof request.spawn === "function" ? request.spawn : undefined;
  const env = request.env ?? process.env;
  const timeoutMs = Number.isSafeInteger(request.timeoutMs) && request.timeoutMs > 0 ? request.timeoutMs : STEP_6M_TIMEOUT_MS;
  const providerName = request.provider ?? STEP_6M_PROVIDER;

  // === Provider setup ===
  let provider;
  try {
    provider = createBrainDecisionProvider({ provider: providerName });
  } catch (error) {
    if (error instanceof HephaestusError && error.code === "STEP_6M_PROVIDER_UNSUPPORTED") {
      return freezeRecord({
        classification: CLASSIFICATIONS.PROVIDER_UNSUPPORTED,
        projectId, allowedRoot, projectPath: null,
        decisionHash: null, decisionValid: false, decisionWritten: false,
        promptHash: null, promptSourceConfirmed: false, promptArtifactWritten: false,
        codexResult: null, codexMutatedFiles: [], forbiddenMutation: false, forbiddenMutatedFiles: [],
        parsed: null, recordTimestamp: now(),
        jsonReportRelative: null, codexArgv: null, timeoutMs,
        ...blockedRecordBase({})
      });
    }
    throw error;
  }

  // === Path resolution ===
  let projectPath = null;
  const bailUnsafe = (label) => freezeRecord({
    classification: CLASSIFICATIONS.UNSAFE_PROJECT,
    projectId, allowedRoot, projectPath: label === "post-resolve" ? projectPath : null,
    decisionHash: null, decisionValid: false, decisionWritten: false,
    promptHash: null, promptSourceConfirmed: false, promptArtifactWritten: false,
    codexResult: null, codexMutatedFiles: [], forbiddenMutation: false, forbiddenMutatedFiles: [],
    parsed: null, recordTimestamp: now(),
    jsonReportRelative: null, codexArgv: null, timeoutMs,
    ...blockedRecordBase({})
  });
  try {
    projectPath = resolveSafePath(allowedRoot, request.projectPath);
  } catch (error) {
    if (error instanceof HephaestusError && PATH_ERROR_CODES.has(error.code)) return bailUnsafe("pre-resolve");
    throw error;
  }
  if (!fs.existsSync(projectPath)) {
    return freezeRecord({
      classification: CLASSIFICATIONS.MISSING_PROJECT,
      projectId, allowedRoot, projectPath,
      decisionHash: null, decisionValid: false, decisionWritten: false,
      promptHash: null, promptSourceConfirmed: false, promptArtifactWritten: false,
      codexResult: null, codexMutatedFiles: [], forbiddenMutation: false, forbiddenMutatedFiles: [],
      parsed: null, recordTimestamp: now(),
      jsonReportRelative: null, codexArgv: null, timeoutMs,
      ...blockedRecordBase({})
    });
  }
  try {
    assertRealPathWithinRoot(allowedRoot, projectPath);
  } catch (error) {
    if (error instanceof HephaestusError && PATH_ERROR_CODES.has(error.code)) return bailUnsafe("post-resolve");
    throw error;
  }

  const decisionAbsolute = resolveSafePath(projectPath, STEP_6M_DECISION_RELATIVE);
  const promptAbsolute = resolveSafePath(projectPath, STEP_6M_PROMPT_RELATIVE);
  const recordTimestamp = now();
  const reportName = request.reportName ?? safeReportNameFromTimestamp(recordTimestamp);
  if (!REPORT_NAME_PATTERN.test(reportName) || !reportName.endsWith(".json")) {
    fail("Computed Step 6M report filename is unsafe.", "INVALID_STEP_6M_REQUEST");
  }
  const jsonReportRelative = `${STEP_6M_REPORT_DIRECTORY_RELATIVE}/${reportName}`;
  const jsonReportAbsolute = resolveSafePath(projectPath, jsonReportRelative);
  const agentOutputAbsolute = resolveSafePath(projectPath, STEP_6M_AGENT_OUTPUT_RELATIVE);
  const allowedPostCodexPaths = new Set([STEP_6M_AGENT_OUTPUT_RELATIVE, jsonReportRelative]);

  // === Get provider decision ===
  const providerDecision = provider.getDecision({ projectId });
  const preValidation = validateStep6mDecision(providerDecision, projectId);
  const decisionCommon = {
    projectId, allowedRoot, projectPath,
    promptHash: null, promptSourceConfirmed: false, promptArtifactWritten: false,
    codexResult: null, codexMutatedFiles: [], forbiddenMutation: false, forbiddenMutatedFiles: [],
    parsed: null, recordTimestamp,
    jsonReportRelative, codexArgv: null, timeoutMs,
    ...blockedRecordBase({})
  };
  if (!preValidation.ok) {
    return freezeRecord({
      classification: CLASSIFICATIONS.PROVIDER_DECISION_INVALID,
      ...decisionCommon,
      decisionHash: null, decisionValid: false, decisionWritten: false
    });
  }

  const decisionText = `${JSON.stringify(providerDecision, null, 2)}\n`;

  const snapshotS0 = snapshotAll(projectPath);

  // === Stage 1: write provider decision ===
  try {
    fs.mkdirSync(path.dirname(decisionAbsolute), { recursive: true });
    fs.writeFileSync(decisionAbsolute, decisionText, { encoding: "utf8" });
  } catch {
    return freezeRecord({
      classification: CLASSIFICATIONS.PROVIDER_DECISION_WRITE_FAILED,
      ...decisionCommon,
      decisionHash: null, decisionValid: false, decisionWritten: false
    });
  }
  const snapshotS1 = snapshotAll(projectPath);
  const decisionChanged = diffSnapshots(snapshotS0, snapshotS1);
  const decisionForbidden = disallowedChanges(decisionChanged, [STEP_6M_DECISION_RELATIVE]);
  if (decisionForbidden.length > 0) {
    return freezeRecord({
      classification: CLASSIFICATIONS.FORBIDDEN_MUTATION,
      ...decisionCommon,
      decisionHash: null, decisionValid: false, decisionWritten: true,
      forbiddenMutation: true, forbiddenMutatedFiles: decisionForbidden
    });
  }

  // === Stage 2: read back + validate ===
  let readback;
  try { readback = fs.readFileSync(decisionAbsolute, { encoding: "utf8" }); }
  catch {
    return freezeRecord({
      classification: CLASSIFICATIONS.PROVIDER_DECISION_READBACK_FAILED,
      ...decisionCommon,
      decisionHash: null, decisionValid: false, decisionWritten: true
    });
  }
  const decisionHash = sha256Hex(readback);
  if (readback !== decisionText) {
    return freezeRecord({
      classification: CLASSIFICATIONS.PROVIDER_DECISION_MISMATCH,
      ...decisionCommon,
      decisionHash, decisionValid: false, decisionWritten: true
    });
  }
  let parsedDecision;
  try { parsedDecision = JSON.parse(readback); } catch { parsedDecision = null; }
  const postValidation = validateStep6mDecision(parsedDecision, projectId);
  if (!postValidation.ok) {
    return freezeRecord({
      classification: CLASSIFICATIONS.PROVIDER_DECISION_INVALID,
      ...decisionCommon,
      decisionHash, decisionValid: false, decisionWritten: true
    });
  }

  // === Stage 3: write prompt ===
  const expectedPrompt = buildStep6mPrompt(projectId);
  try {
    fs.mkdirSync(path.dirname(promptAbsolute), { recursive: true });
    fs.writeFileSync(promptAbsolute, expectedPrompt, { encoding: "utf8" });
  } catch {
    return freezeRecord({
      classification: CLASSIFICATIONS.PROMPT_WRITE_FAILED,
      ...decisionCommon,
      decisionHash, decisionValid: true, decisionWritten: true
    });
  }
  const snapshotS2 = snapshotAll(projectPath);
  const promptChanged = diffSnapshots(snapshotS1, snapshotS2);
  const promptForbidden = disallowedChanges(promptChanged, [STEP_6M_PROMPT_RELATIVE]);
  if (promptForbidden.length > 0) {
    return freezeRecord({
      classification: CLASSIFICATIONS.FORBIDDEN_MUTATION,
      ...decisionCommon,
      decisionHash, decisionValid: true, decisionWritten: true,
      promptArtifactWritten: true,
      forbiddenMutation: true, forbiddenMutatedFiles: promptForbidden
    });
  }
  let promptReadback;
  try { promptReadback = fs.readFileSync(promptAbsolute, { encoding: "utf8" }); }
  catch {
    return freezeRecord({
      classification: CLASSIFICATIONS.PROMPT_READBACK_FAILED,
      ...decisionCommon,
      decisionHash, decisionValid: true, decisionWritten: true,
      promptArtifactWritten: true
    });
  }
  const promptHash = sha256Hex(promptReadback);
  if (promptReadback !== expectedPrompt) {
    return freezeRecord({
      classification: CLASSIFICATIONS.PROMPT_MISMATCH,
      ...decisionCommon,
      decisionHash, decisionValid: true, decisionWritten: true,
      promptHash, promptArtifactWritten: true
    });
  }

  const codexArgv = buildStep6mArgv(promptReadback);
  assertArgvSafety(codexArgv, promptReadback);

  // === Stage 4: Codex run ===
  const codexResult = runCodexSpawn({ spawn, argv: codexArgv, projectPath, env, timeoutMs });
  const snapshotS3 = snapshotAll(projectPath);
  const codexChanged = diffSnapshots(snapshotS2, snapshotS3);
  if (codexChanged.length > 0) {
    return freezeRecord({
      classification: CLASSIFICATIONS.CODEX_MUTATED_PROJECT,
      ...decisionCommon,
      decisionHash, decisionValid: true, decisionWritten: true,
      promptHash, promptSourceConfirmed: true, promptArtifactWritten: true,
      codexResult, codexMutatedFiles: codexChanged, codexArgv
    });
  }
  const codexClassification = classifyCodex(codexResult);
  if (codexClassification !== null) {
    return freezeRecord({
      classification: codexClassification,
      ...decisionCommon,
      decisionHash, decisionValid: true, decisionWritten: true,
      promptHash, promptSourceConfirmed: true, promptArtifactWritten: true,
      codexResult, codexArgv
    });
  }
  if (!codexResult.markerInOutput) {
    return freezeRecord({
      classification: CLASSIFICATIONS.MARKER_MISSING,
      ...decisionCommon,
      decisionHash, decisionValid: true, decisionWritten: true,
      promptHash, promptSourceConfirmed: true, promptArtifactWritten: true,
      codexResult, codexArgv
    });
  }
  const parsed = parseStep6mReport(codexResult.combined, {
    expectedProjectId: projectId, expectedPromptSource: STEP_6M_PROMPT_RELATIVE
  });
  if (!parsed.ok) {
    return freezeRecord({
      classification: CLASSIFICATIONS.MARKER_MALFORMED,
      ...decisionCommon,
      decisionHash, decisionValid: true, decisionWritten: true,
      promptHash, promptSourceConfirmed: true, promptArtifactWritten: true,
      codexResult, parsed, codexArgv
    });
  }

  // === Stage 5: output artifacts ===
  const successArgs = {
    projectId, classification: CLASSIFICATIONS.PASS,
    decisionRelative: STEP_6M_DECISION_RELATIVE, decisionHash, decisionValid: true,
    promptRelative: STEP_6M_PROMPT_RELATIVE, promptHash, promptSourceConfirmed: true,
    markerCaptured: true, reportValid: true,
    codexMutatedProject: false, codexMutatedFiles: [],
    forbiddenMutation: false, forbiddenMutatedFiles: [],
    filesInspected: parsed.report.filesInspected, summary: parsed.report.summary,
    recordTimestamp, jsonReportRelative,
    agentOutputRelative: STEP_6M_AGENT_OUTPUT_RELATIVE,
    stdout: codexResult.stdout, stderr: codexResult.stderr,
    codexExitCode: codexResult.exitCode, codexTimedOut: codexResult.timedOut,
    timeoutMs
  };
  const jsonReport = buildJsonReport(successArgs);
  const agentOutputMarkdown = buildAgentOutputMarkdown(successArgs);

  try {
    fs.mkdirSync(path.dirname(jsonReportAbsolute), { recursive: true });
    fs.writeFileSync(jsonReportAbsolute, `${JSON.stringify(jsonReport, null, 2)}\n`, { encoding: "utf8" });
    fs.writeFileSync(agentOutputAbsolute, agentOutputMarkdown, { encoding: "utf8" });
  } catch {
    return freezeRecord({
      classification: CLASSIFICATIONS.ARTIFACT_WRITE_FAILED,
      ...decisionCommon,
      decisionHash, decisionValid: true, decisionWritten: true,
      promptHash, promptSourceConfirmed: true, promptArtifactWritten: true,
      codexResult, parsed, codexArgv
    });
  }

  const snapshotS4 = snapshotAll(projectPath);
  const postCodexChanged = diffSnapshots(snapshotS3, snapshotS4);
  const postCodexForbidden = disallowedChanges(postCodexChanged, allowedPostCodexPaths);
  if (postCodexForbidden.length > 0) {
    return freezeRecord({
      classification: CLASSIFICATIONS.FORBIDDEN_MUTATION,
      ...decisionCommon,
      decisionHash, decisionValid: true, decisionWritten: true,
      promptHash, promptSourceConfirmed: true, promptArtifactWritten: true,
      codexResult, parsed, codexArgv,
      forbiddenMutation: true, forbiddenMutatedFiles: postCodexForbidden
    });
  }

  return freezeRecord({
    classification: CLASSIFICATIONS.PASS,
    ...decisionCommon,
    decisionHash, decisionValid: true, decisionWritten: true,
    promptHash, promptSourceConfirmed: true, promptArtifactWritten: true,
    codexResult, parsed, codexArgv,
    artifactsWritten: true, agentOutputRelative: STEP_6M_AGENT_OUTPUT_RELATIVE
  });
}

